# Ejercicio 5. Números positivos y negativos

numero = int(input("Escriba un numero: " ))
if numero < 0 : 
        print("Es un numero negativo")
if numero == 0 :
        print("El numero es 0")
if numero > 0 :
        print("El numero es positivo")